void PcKeyboard::reset(void)
{
	clearKeys();
	m_queue.clear();
	m_cmdActive = 0;
	m_scanSet = 1;
	m_typematic = 0x2C;
	m_enabled = true;
}

int PcKeyboard::handleEvent(SDL_Event &e)
{
        bool b = false;
        int rv = 0;
	int n;
        Uint16 *km;
        SDLKey keysym = SDLK_UNKNOWN;

        if (e.type == SDL_KEYDOWN || e.type == SDL_KEYUP)
        {
                b = (e.type == SDL_KEYDOWN);
                keysym = e.key.keysym.sym;
     
		for (n = 0; n < sizeof(kc_maps); n += 7)
		{
			if (keysym == kc_maps[n])
			{
				n++;
				if (!b) n++;
				n = kc_maps[n];
				switch(n)	/* Special cases... */
				{
					case 0xFFFFFFFE:	// PrtSc up
					pushKey(0xE0);
					pushKey(0xF0);
					pushKey(0x12);
					pushKey(0xE0);
					pushKey(0xF0);
					pushKey(0x7C);
					return 1;	

					case 0xFFFFFFF9:	// PAUSE down
					pushKey(0xE1);
					pushKey(0x14);
					pushKey(0x77);
					pushKey(0xE1);
					pushKey(0xF0);
					pushKey(0x14);
					pushKey(0xF0);
					pushKey(0x77);
					return 1;	
					case 0xFFFFFFF8:	// PAUSE up
					return 1;
				}	
				if (n & 0xFF000000) 
				{
					pushKey((n >> 24) & 0xFF);
					n &= 0xFFFFFF;	
				}
				if (n & 0xFF0000) 
				{
					pushKey((n >> 16) & 0xFF);
					n &= 0xFFFF;	
				}
				if (n & 0xFF00) 
				{
					pushKey((n >> 8) & 0xFF);
					n &= 0xFF;
				}
				pushKey(n);
				return 1;
			}
		}
	}

        switch(e.type)
        {
                case SDL_QUIT: rv = -99; break;
                case SDL_KEYDOWN:
                	//m_keyPress = true;
                        rv = 1;
                        break;
                case SDL_KEYUP:
                        //m_keyPress = true;
                        rv = 1;
                        break;
        }
	return 0;
}
